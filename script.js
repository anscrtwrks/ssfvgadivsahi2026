function fitText(){

const container =
document.getElementById(
"quoteContainer"
);

let size = 34;

quoteElement.style.fontSize =
size + "px";

while(

quoteElement.scrollHeight >
container.clientHeight

&&

size > 16

){

size--;

quoteElement.style.fontSize =
size + "px";

}

}

function randomQuote(){

const quote =

quotes[
Math.floor(
Math.random()
*
quotes.length
)
];

quoteElement.innerText =
quote;

fitText();

}

randomQuote();

